const ORDERINGS = [
  ['intro', 'history', 'buying'],
  ['intro', 'buying', 'history'],
  ['history', 'intro', 'buying'],
  ['buying', 'intro', 'history']
];

function renderAbout(category, facts) {
  const pools = POOLS[category] || POOLS.concert;
  const seed = fnv1a(facts.slug || facts.name || 'x');
  const order = ORDERINGS[seed % ORDERINGS.length];
  const parts = [];
  order.forEach((key, i) => {
    const pool = pools[key];
    if (!pool) return;
    const start = (seed >>> (i * 3)) % pool.length; // >>> — unsigned, never negative
    // Try the seeded variant first; fall back through the pool if facts are missing
    for (let j = 0; j < pool.length; j++) {
      const sentence = pool[(start + j) % pool.length](facts);
      if (sentence) { parts.push(sentence); break; }
    }
  });
  return parts.join(' ');
}

