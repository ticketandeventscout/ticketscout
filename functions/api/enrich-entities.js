// ===========================
// TicketScout — Entity enrichment + Mad Libs copy framework (Phase 4.3E)
// Runs as a Cloudflare Pages Function at /api/enrich-entities
//
// For each entity in sitemap:registry, fetches licence-clean facts and
// generates a unique, seeded "About" paragraph:
//
//   football → Wikidata (CC0): stadium, capacity, city, league, founded
//   concert  → MusicBrainz (core data CC0): type, origin, active-since, genres
//   sports   → Wikidata (CC0): sport, league, venue, capacity, city, founded
//              (clubs) or sport, citizenship, born (individual athletes)
//   theatre  → no reliable open source yet — minimal record written so the
//              cursor completes; copy framework still produces a paragraph
//              from name alone (short, honest, non-templated ordering)
//
// Output per entity → KV: entity:meta:{category}:{slug}
//   { slug, category, facts: {...}, aboutText, source, licence, fetchedAt }
//
// aboutText is generated by seeded sentence pools (fnv1a(slug) picks the
// paragraph ordering AND the variant of every sentence) so no two pages
// shingle-match. Facts-only — NO prices baked in (prices go stale; the
// live price sentence is rendered at page level from price:summary KV).
//
// Usage:
//   ?trigger=1                       — process next batch from the cursor
//   ?trigger=1&limit=10              — batch size (default 10, max 15)
//   ?trigger=1&slug=arsenal&category=football   — single entity (debug)
//   ?trigger=1&force=1               — re-fetch even if meta already exists
//   &dry=1                           — fetch + generate but write nothing
//
// Cron: piggyback weekly (e.g. Mon 01:00). Each run enriches ~10 entities;
// 565 known slugs ≈ complete in ~8 weeks, then top-up only (new entities).
// Rate limits respected: MusicBrainz 1 req/s (we sleep 1100ms), Wikidata
// SPARQL is fine at this volume. Both require a descriptive User-Agent.
// ===========================

const CURSOR_KEY = 'enrich:cursor';         // { category, offset }
const META_PREFIX = 'entity:meta:';
const UA = 'TicketScoutBot/1.0 (https://ticketscout.co.uk; contact@ticketscout.co.uk)';
const CATEGORIES = ['football', 'concert', 'theatre', 'sports'];

export async function onRequestGet({ request, env }) {
  const url = new URL(request.url);
  if (url.searchParams.get('trigger') !== '1') {
    return new Response([
      'TicketScout entity enrichment — usage:',
      '  ?trigger=1                    — process next batch (cursor-based)',
      '  ?trigger=1&limit=10           — batch size (max 15)',
      '  ?trigger=1&slug=X&category=Y  — enrich a single entity',
      '  ?trigger=1&force=1            — overwrite existing meta',
      '  &dry=1                        — no KV writes'
    ].join('\n'), { headers: { 'Content-Type': 'text/plain' } });
  }

  const kv = env.GIGSBERG_KV;
  if (!kv) return json({ error: 'Missing GIGSBERG_KV' }, 500);

  const dryRun = url.searchParams.get('dry') === '1';
  const force  = url.searchParams.get('force') === '1';
  const limit  = Math.min(parseInt(url.searchParams.get('limit') || '10', 10) || 10, 15);
  const oneSlug = url.searchParams.get('slug');
  const oneCat  = url.searchParams.get('category');

  // ── Single-entity debug path ──────────────────────────────────────────
  if (oneSlug) {
    if (!CATEGORIES.includes(oneCat)) return json({ error: 'category must be football|concert|theatre|sports' }, 400);
    const rec = await entityRecord(kv, oneCat, oneSlug);
    const result = await enrichOne(oneCat, oneSlug, rec.name, rec.genre);
    if (!dryRun && result.meta) await kv.put(META_PREFIX + `${oneCat}:${oneSlug}`, JSON.stringify(result.meta));
    return json({ ...result, dryRun }, 200);
  }

  // ── Cursor batch path ─────────────────────────────────────────────────
  let registry = null;
  try { const r = await kv.get('sitemap:registry'); if (r) registry = JSON.parse(r); } catch {}
  if (!registry || !registry.sections) {
    return json({ error: 'sitemap:registry not built — run /api/discover-pages?trigger=1&phase=build-registry first' }, 503);
  }

  let cursor = { category: 'football', offset: 0 };
  try { const c = await kv.get(CURSOR_KEY); if (c) cursor = JSON.parse(c); } catch {}
  if (!CATEGORIES.includes(cursor.category)) cursor = { category: 'football', offset: 0 };

  const processed = [], skipped = [], errors = [];
  let remaining = limit;

  while (remaining > 0) {
    const slugs = Object.keys(registry.sections[cursor.category] || {}).sort();
    if (cursor.offset >= slugs.length) {
      // Advance to next category; stop after the last one (top-up next run)
      const idx = CATEGORIES.indexOf(cursor.category);
      if (idx === CATEGORIES.length - 1) { cursor = { category: 'football', offset: 0, cycleDone: true }; break; }
      cursor = { category: CATEGORIES[idx + 1], offset: 0 };
      continue;
    }

    const slug = slugs[cursor.offset];
    cursor.offset++;

    // Skip already-enriched entities unless forced
    if (!force) {
      try { if (await kv.get(META_PREFIX + `${cursor.category}:${slug}`)) { skipped.push(slug); continue; } } catch {}
    }

    const rec = await entityRecord(kv, cursor.category, slug);
    try {
      const result = await enrichOne(cursor.category, slug, rec.name, rec.genre);
      if (result.meta) {
        if (!dryRun) await kv.put(META_PREFIX + `${cursor.category}:${slug}`, JSON.stringify(result.meta));
        processed.push({ slug, category: cursor.category, factCount: Object.keys(result.meta.facts).length });
      } else {
        errors.push({ slug, error: result.error || 'no data' });
      }
    } catch (err) {
      errors.push({ slug, error: String(err) });
    }
    remaining--;

    // MusicBrainz politeness: 1 request/second
    if (cursor.category === 'concert') await sleep(1100);
  }

  if (!dryRun) await kv.put(CURSOR_KEY, JSON.stringify(cursor));
  return json({
    processed, skipped: skipped.length, errors, cursor, dryRun,
    message: cursor.cycleDone
      ? 'Full cycle complete — cursor reset; future runs top-up new entities only.'
      : 'Batch complete. Re-run to continue from cursor.'
  }, 200);
}

// ── Entity record lookup (KV record → fallback to de-slugged name) ──────
// Returns { name, genre }. Sports enrichment needs the genre: it tells us
// which sport the Wikidata match MUST agree with, so a wrong QID can be
// rejected instead of publishing another entity's stadium.
async function entityRecord(kv, category, slug) {
  const prefixes = {
    football: 'football:team:', concert: 'concert:artist:',
    theatre: 'theatre:show:',   sports:  'sports:team:'
  };
  try {
    const raw = await kv.get(prefixes[category] + slug);
    if (raw) {
      const d = JSON.parse(raw);
      if (d.name) return { name: d.name, genre: d.genre || '' };
    }
  } catch {}
  return {
    name: slug.replace(/-/g, ' ').replace(/\b\w/g, c => c.toUpperCase()),
    genre: ''
  };
}

// ── Per-category enrichment ──────────────────────────────────────────────
async function enrichOne(category, slug, name, genre) {
  let facts = {}, source = 'none', licence = 'n/a';
  if (category === 'football') {
    facts = await wikidataClubFacts(name);
    source = 'wikidata'; licence = 'CC0';
  } else if (category === 'concert') {
    facts = await musicbrainzArtistFacts(name);
    source = 'musicbrainz'; licence = 'CC0';
  } else if (category === 'sports') {
    facts = await wikidataSportsFacts(name, genre);
    source = 'wikidata'; licence = 'CC0';
  }
  facts = facts || {};
  // For UK clubs, country is implicit — suppress so it doesn't
  // appear in copy (e.g. avoid "country: United Kingdom" in sentences)
  if (facts.country === 'United Kingdom') delete facts.country;
  facts.name = name;
  facts.slug = slug;

  const aboutText = renderAbout(category, facts);
  return {
    meta: {
      slug, category, facts, aboutText, source, licence,
      fetchedAt: new Date().toISOString()
    }
  };
}

// ── Wikidata: sports entity facts (clubs AND individual athletes) ───────
// Football has its own function because it can hard-filter on "association
// football club". A sports entity might be a basketball franchise, an ice
// hockey club, or a single fighter, so this accepts either a sports team or
// a person with an athlete occupation, then VERIFIES the sport matches the
// genre we already believe. A mismatch means we matched the wrong QID —
// "Anaheim Ducks" the hockey team vs some unrelated item — and we discard
// everything rather than publish another entity's venue as fact.
const SPORT_ALIASES = {
  'basketball':        ['basketball'],
  'mma':               ['mixed martial arts', 'martial arts', 'combat sport'],
  'ice hockey':        ['ice hockey', 'hockey'],
  'rugby':             ['rugby', 'rugby union', 'rugby league', 'rugby football'],
  'handball':          ['handball'],
  'american football': ['american football', 'gridiron'],
  'baseball':          ['baseball'],
  'boxing':            ['boxing'],
  'tennis':            ['tennis'],
  'cricket':           ['cricket'],
  'motorsport':        ['motorsport', 'auto racing', 'motorsports', 'formula one', 'racing'],
  'golf':              ['golf'],
  'wrestling':         ['wrestling', 'professional wrestling', 'sumo']
};

function sportMatchesGenre(sportLabel, genre) {
  // No genre to check against, or Wikidata gave no sport: can't disprove,
  // so allow it. We only reject on POSITIVE evidence of a mismatch.
  if (!genre || !sportLabel) return true;
  const aliases = SPORT_ALIASES[String(genre).toLowerCase().trim()];
  if (!aliases) return true;
  const s = String(sportLabel).toLowerCase();
  return aliases.some(a => s.includes(a) || a.includes(s));
}

async function wikidataSportsFacts(name, genre) {
  const searchUrl = 'https://www.wikidata.org/w/api.php?action=wbsearchentities&format=json' +
    `&language=en&type=item&limit=5&search=${encodeURIComponent(name)}`;
  const sr = await fetch(searchUrl, { headers: { 'User-Agent': UA } });
  if (!sr.ok) return {};
  const sd = await sr.json();
  const qids = (sd.search || []).map(x => x.id);
  if (!qids.length) return {};

  const values = qids.map(q => `wd:${q}`).join(' ');
  const sparql = `
    SELECT ?item ?kind ?sportLabel ?leagueLabel ?venueLabel ?capacity ?cityLabel
           ?founded ?countryLabel ?citizenshipLabel ?born WHERE {
      VALUES ?item { ${values} }
      {
        ?item wdt:P31/wdt:P279* wd:Q12973014 .
        BIND("team" AS ?kind)
      } UNION {
        ?item wdt:P106/wdt:P279* wd:Q2066131 .
        BIND("person" AS ?kind)
      }
      OPTIONAL { ?item wdt:P641 ?sport . }
      OPTIONAL { ?item wdt:P118 ?league . }
      OPTIONAL { ?item wdt:P115 ?venue .
                 OPTIONAL { ?venue wdt:P1083 ?capacity . }
                 OPTIONAL { ?venue wdt:P131 ?city . } }
      OPTIONAL { ?item wdt:P571 ?founded . }
      OPTIONAL { ?item wdt:P17  ?country . }
      OPTIONAL { ?item wdt:P27  ?citizenship . }
      OPTIONAL { ?item wdt:P569 ?born . }
      SERVICE wikibase:label { bd:serviceParam wikibase:language "en". }
    } LIMIT 10`;

  const qr = await fetch('https://query.wikidata.org/sparql?format=json&query=' + encodeURIComponent(sparql),
    { headers: { 'User-Agent': UA, 'Accept': 'application/sparql-results+json' } });
  if (!qr.ok) return {};
  const qd = await qr.json();
  const rows = qd.results?.bindings || [];
  if (!rows.length) return {};

  // Prefer, in order: a candidate whose sport agrees with our genre, keeping
  // the label-search ranking. Only if none agree do we give up — we do NOT
  // fall back to rows[0], because an unverified match is what produces
  // confidently wrong pages.
  const byQid = new Map();
  for (const r of rows) {
    const q = r.item.value.split('/').pop();
    if (!byQid.has(q)) byQid.set(q, r);
  }
  let row = null;
  for (const q of qids) {
    const r = byQid.get(q);
    if (r && sportMatchesGenre(r.sportLabel?.value, genre)) { row = r; break; }
  }
  if (!row) return {};

  const facts = { entityType: row.kind?.value === 'person' ? 'Person' : 'Team' };
  if (row.sportLabel?.value)       facts.sport    = row.sportLabel.value;
  if (row.leagueLabel?.value)      facts.league   = row.leagueLabel.value;
  if (row.venueLabel?.value)       facts.venue    = row.venueLabel.value;
  if (row.capacity?.value)         facts.capacity = parseInt(row.capacity.value, 10) || undefined;
  if (row.cityLabel?.value)        facts.city     = row.cityLabel.value;
  if (row.founded?.value)          facts.founded  = row.founded.value.slice(0, 4);
  if (row.countryLabel?.value)     facts.country  = row.countryLabel.value;
  if (row.citizenshipLabel?.value) facts.nationality = row.citizenshipLabel.value;
  if (row.born?.value)             facts.born     = row.born.value.slice(0, 4);
  facts.wikidataId = row.item.value.split('/').pop();

  // A "team" with a birth year, or a "person" with a stadium, means the
  // union matched oddly — drop the contradictory fields rather than render
  // "founded in 1991" about a boxer.
  if (facts.entityType === 'Person') { delete facts.capacity; delete facts.founded; }
  else { delete facts.born; delete facts.nationality; }

  return facts;
}

// ── Wikidata: football club facts (2 calls: entity search + SPARQL) ─────
async function wikidataClubFacts(name) {
  // 1. Find candidate QIDs by label
  // Ambiguous one-word names ("Arsenal", "Chelsea") still resolve correctly:
  // the SPARQL step filters candidates to instance-of football club.
  const searchUrl = 'https://www.wikidata.org/w/api.php?action=wbsearchentities&format=json' +
    `&language=en&type=item&limit=5&search=${encodeURIComponent(name)}`;
  const sr = await fetch(searchUrl, { headers: { 'User-Agent': UA } });
  if (!sr.ok) return {};
  const sd = await sr.json();
  const qids = (sd.search || []).map(x => x.id);
  if (!qids.length) return {};

  // 2. One SPARQL query over all candidates — first that IS a football club wins
  const values = qids.map(q => `wd:${q}`).join(' ');
  const sparql = `
    SELECT ?item ?stadiumLabel ?capacity ?cityLabel ?leagueLabel ?founded ?countryLabel WHERE {
      VALUES ?item { ${values} }
      ?item wdt:P31/wdt:P279* wd:Q476028 .           # instance of association football club
      OPTIONAL { ?item wdt:P115 ?stadium .
                 OPTIONAL { ?stadium wdt:P1083 ?capacity . }
                 OPTIONAL { ?stadium wdt:P131 ?city . } }
      OPTIONAL { ?item wdt:P118 ?league . }
      OPTIONAL { ?item wdt:P571 ?founded . }
      OPTIONAL { ?item wdt:P17 ?country . }
      SERVICE wikibase:label { bd:serviceParam wikibase:language "en". }
    } LIMIT 5`;
  const qr = await fetch('https://query.wikidata.org/sparql?format=json&query=' + encodeURIComponent(sparql),
    { headers: { 'User-Agent': UA, 'Accept': 'application/sparql-results+json' } });
  if (!qr.ok) return {};
  const qd = await qr.json();
  const rows = qd.results?.bindings || [];
  if (!rows.length) return {};

  // Keep candidate order from the label search (best match first)
  const byQid = new Map(rows.map(r => [r.item.value.split('/').pop(), r]));
  let row = null;
  for (const q of qids) { if (byQid.has(q)) { row = byQid.get(q); break; } }
  if (!row) row = rows[0];

  const facts = {};
  if (row.stadiumLabel?.value) facts.stadium  = row.stadiumLabel.value;
  // Normalise city: Wikidata often returns "London Borough of Islington" etc.
  const LONDON_DISTRICTS = new Set([
    'Islington','Camden','Hackney','Tower Hamlets','Southwark','Lambeth',
    'Hammersmith and Fulham','Fulham','Wandsworth','Westminster',
    'Kensington and Chelsea','Lewisham','Greenwich','Bexley','Bromley',
    'Croydon','Sutton','Merton','Kingston upon Thames','Richmond upon Thames',
    'Hounslow','Ealing','Hillingdon','Harrow','Brent','Barnet','Haringey',
    'Enfield','Waltham Forest','Redbridge','Newham','Barking and Dagenham',
    'Havering','City of London'
  ]);
  const normaliseCity = c => {
    c = c.replace(/^London Borough of /, '').replace(/^Royal Borough of /, '')
         .replace(/^Borough of /, '').replace(/^City of (?!London)/, '')
         .replace(/^Metropolitan Borough of /, '');
    if (LONDON_DISTRICTS.has(c)) return 'London';
    // "Frankfurt am Main" → "Frankfurt" for cleaner prose
    c = c.replace(/ am Main$/, '');
    return c;
  };
  if (row.capacity?.value)     facts.capacity = parseInt(row.capacity.value, 10) || undefined;
  if (row.cityLabel?.value)    facts.city     = normaliseCity(row.cityLabel.value);
  if (row.leagueLabel?.value)  facts.league   = row.leagueLabel.value;
  if (row.founded?.value)      facts.founded  = row.founded.value.slice(0, 4);
  if (row.countryLabel?.value) facts.country  = row.countryLabel.value;
  facts.wikidataId = row.item.value.split('/').pop();
  return facts;
}

// ── MusicBrainz: artist facts (1 call) ───────────────────────────────────
async function musicbrainzArtistFacts(name) {
  const u = `https://musicbrainz.org/ws/2/artist/?query=artist:${encodeURIComponent(JSON.stringify(name))}&fmt=json&limit=3`;
  const r = await fetch(u, { headers: { 'User-Agent': UA } });
  if (!r.ok) return {};
  const d = await r.json();
  const top = (d.artists || [])[0];
  if (!top || (top.score || 0) < 90) return {};   // only confident matches

  const facts = { mbid: top.id };
  if (top.type)              facts.artistType  = top.type;               // Person | Group
  if (top.area?.name)        facts.origin      = top.area.name;
  if (top['begin-area']?.name) facts.beginArea = top['begin-area'].name;
  if (top['life-span']?.begin) facts.activeSince = top['life-span'].begin.slice(0, 4);
  const tags = (top.tags || []).sort((a, b) => (b.count || 0) - (a.count || 0)).slice(0, 3).map(t => t.name);
  if (tags.length) facts.genres = tags;
  return facts;
}

// ── Mad Libs framework (Phase 4.3E interpolation) ────────────────────────
// fnv1a(slug) seeds BOTH the paragraph ordering and each sentence variant,
// so the connective tissue never shingle-matches across pages. Sentences
// self-omit when their facts are missing — no "undefined" ever renders.
function fnv1a(str) {
  let h = 0x811c9dc5;
  for (let i = 0; i < str.length; i++) { h ^= str.charCodeAt(i); h = Math.imul(h, 0x01000193); }
  return h >>> 0;
}

